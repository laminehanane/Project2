package Review1;

public class E5Arithmetic {
    public static void main(String[] args) {
        int num1=20;
        int num2=10;
        // addition
        System.out.println(num1+num2);
        //Subtraction
        System.out.println(num1-num2);
        //multiplication
        System.out.println(num1*num2);
        //division
        System.out.println(num1/num2);
        //modulus
        System.out.println(num1%num2);





    }
}
